/* Make a matrix of the given size and initialise all values to x. */
void mk_mat(queue &q, unsigned int row, unsigned int col, float x, float *result) {
  {
    buffer r_buf(result, range(row, col));
    q.submit([&](auto &h) {
      accessor r(r_buf, h, write_only);

      h.parallel_for(range(row, col), [=](auto index) {
        r[index] = x;
      });
    });
  }
}

/* Make a matrix of the given size and initliase all values randomly between 0 and 1. */
void mk_mat_rand(queue &q, unsigned int row, unsigned int col, float *result) {
  {
    buffer r_buf(result, range(row, col));
    q.submit([&](auto &h) {
      accessor r(r_buf, h, write_only);

      h.parallel_for(range(row, col), [=](auto index) {
        oneapi::dpl::minstd_rand engine(114, index.get_linear_id());
        oneapi::dpl::uniform_real_distribution<float> distr;
        r[index] = distr(engine);
      });
    });
  }
}

/* Show the matrix. If snapshot = true, only shwo the first 3 * 3 corner. */
void peek(unsigned int row, unsigned int col, float *matrix, bool snapshot) {
  for (unsigned int i = 0; i < (snapshot && row > 3 ? 3 : row); i++) {
    for (unsigned int j = 0; j < (snapshot && col > 3 ? 3 : col); j++) {
      cout << std::fixed << matrix[i * col + j] << "\t";
    }
    if (col > 3 && snapshot) {
      cout << "...";
    }
    cout << "\n";
  }
  if (row > 3 && snapshot) {
    cout << "...";
  }
  cout << "\n";
}

/* Carry out MaxPool on the given matrices (C * H * W) with the given stride. */
void max_pool(queue &q, unsigned int chn, unsigned int row, unsigned int col, float *matrix, unsigned int stride,
    unsigned int *new_row, unsigned int *new_col, float *result) {
  const unsigned int nr = 1 + (row - 1) / stride;
  const unsigned int nc = 1 + (col - 1) / stride;
  {
    buffer m_buf(matrix, range(chn, row, col));
    buffer r_buf(result, range(chn, nr, nc));
    q.submit([&](auto &h) {
      accessor m(m_buf, h, read_only);
      accessor r(r_buf, h, write_only);

      h.parallel_for(range(chn, nr, nc), [=](auto index) {
        unsigned int _chn = index[0];
        unsigned int max_r = (index[1] + 1) * stride > row ? row : (index[1] + 1) * stride;
        unsigned int max_c = (index[2] + 1) * stride > col ? col : (index[2] + 1) * stride;
        float f = numeric_limits<float>::lowest();
        float cur;
        for (unsigned int i = index[1] * stride; i < max_r; i++) {
          for (unsigned int j = index[2] * stride; j < max_c; j++) {
            cur = m[_chn][i][j];
            f = f > cur ? f : cur;
          }
        }
        r[index] = f;
      });
    });
  }

  if (new_row) *new_row = nr;
  if (new_col) *new_col = nc;
}

void relu(queue &q, unsigned int length, float *matrix, float *result) {
  {
    buffer m_buf(matrix, range(length));
    buffer r_buf(result, range(length));
    q.submit([&](auto &h) {
      accessor m(m_buf, h, read_only);
      accessor r(r_buf, h, write_only);

      h.parallel_for(range(length), [=](auto index) {
        r[index] = r[index] >= 0.0 ? r[index] : 0.0;
      });
    });
  }
}

/* Carry out convolution (no padding) between the matrices (C * W * H) and the filter (D * K * K).
   size must be an odd number and less than min(row, col). */
void conv(queue &q, unsigned int chn, unsigned int row, unsigned int col, float *matrix, unsigned int d, unsigned int size, float *filter,
    float *biases, unsigned int *new_row, unsigned int *new_col, float *result) {
  const unsigned int nr = row - size + 1;
  const unsigned int nc = col - size + 1;
  {
    buffer m_buf(matrix, range(chn, row, col));
    buffer f_buf(filter, range(d, size, size));
    buffer b_buf(biases, range(d));
    buffer r_buf(result, range(d, nr, nc));
    q.submit([&](auto &h) {
      accessor m(m_buf, h, read_only);
      accessor f(f_buf, h, read_only);
      accessor b(b_buf, h, read_only);
      accessor r(r_buf, h, write_only);

      h.parallel_for(range(d, nr, nc), [=](auto index) {
        float sum = 0.0f;
        for (unsigned int c = 0; c < chn; c++) {
          for (unsigned int i = 0; i < size; i++) {
            for (unsigned int j = 0; j < size; j++) {
              sum += f[index[0]][i][j] * m[c][index[1] + i][index[2] + j];
            }
          }
        }
        r[index] = sum + b[index[0]];
      });
    });
  }

  if (new_row) *new_row = nr;
  if (new_col) *new_col = nc;
}

/* Carry out the calculation for a fully-connected layer. */
void fully_connected(queue &q, unsigned int c_in, unsigned int c_out, float *vector,
    float *weights, float *bias, float *result) {
  {
    buffer v_buf(vector, range(c_in));
    buffer w_buf(weights, range(c_out, c_in));
    buffer b_buf(bias, range(c_out));
    buffer r_buf(result, range(c_out));
    q.submit([&](auto &h) {
      accessor v(v_buf, h, read_only);
      accessor w(w_buf, h, read_only);
      accessor b(b_buf, h, read_only);
      accessor r(r_buf, h, write_only);

      h.parallel_for(range(c_out), [=](auto index) {
        float sum = 0.0f;
        for (unsigned int i = 0; i < c_in; i++) {
          sum += v[i] * w[index][i];
        }
        r[index] = sum + b[index];
      });
    });
  }
}

